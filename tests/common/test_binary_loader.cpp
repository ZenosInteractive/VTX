// Coverage:
//   - BinaryCursor: typed reads, length-prefixed strings, c-strings, sub-cursors,
//     bounds checking (throws on overrun), endian swap.
//   - GenericBinaryLoader: dispatch into BinaryBinding<Tag>::Transfer, walking
//     the cursor with Read<T> and feeding each value through LoadField into the
//     correct PropertyContainer slot.

